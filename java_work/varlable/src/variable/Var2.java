package variable;

public class Var2 {

	public static void main(String[] args) {
		int a; //변수선언
		a = 20;
		
		System.out.println(a);
		System.out.println(a);
		System.out.println(a);
		/*
		 * int a 숫자정수를 보관할수있는 데이터 저장소
		 * 이렇게 변수를 만드는것이 변수선언
		 * 변수 a에는 숫자 정수를 보관할수있음
		 * 숫자 정수 뿐만 아니라 문자.소수와같이 다양한 값을 저장할수있음
		 */
	}
}
