package ex01;
/*
 * 여러 줄을 주석 처리
 * ctrl+z 뒤로 가기
 * ctrl+y 앞으로 가기
 * ctrl+w 창닫는 단축키
 */

public class ex03 {
	public static void main(String[] args) {
		
		System.out.println("10+3 = " + (10+3));
		System.out.println("50/3 = " + 50 / 3);
		System.out.println("50% = " + 50 % 3);
		System.out.println("3*4 = " + 3 * 4);
		System.out.println("10-7 = " + (10-7));
		
		// ctrl + / 주석처리 및 해제
		//+ - 는 () 쓰기
		
	}

}
